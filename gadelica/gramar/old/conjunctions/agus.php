<h1>agus</h1>
<p><strong>agus</strong> is a coordinating conjunction corresponding to English ‘and’.</p>
<hr/>
<p>Parataxis:</p>
<ul>
  <li><strong>Seas Iain <mark>agus</mark> shuidh Anna.</strong> ‘John stood up and Anna sat down.’ [DM92 163]</li>
</ul>
<p>Contemporal clauses (non-finite, progressive):</p>
<ul>
  <li><strong>Seas Iain <mark>agus</mark> Anna <mark>a’</mark> suidhe.</strong> ‘John stood up whilst Anna was sitting down.’ [DM92 164]</li>
</ul>
<p>Subsequence/consequence clauses (non-finite, perfective):</p>
<ul>
  <li><strong>Seas Iain <mark>agus</mark> Anna <mark>air</mark> suidhe.</strong> ‘John stood up, Anna having sat down.’ [DM92 164]</li>
</ul>


