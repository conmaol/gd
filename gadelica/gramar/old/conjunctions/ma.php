<h1>ma</h1>
<p><strong>ma</strong> is a <a href="../xx/subordinatingConjunctions.php">subordinating conjunction</a>, typically governing a <a href="../xx/tense.php">future</a> <a href="../xx/dependency.php">relative</a> verb form, and usually modifying a future tense verb form or particle.</p>
<p><strong>ma</strong> generally corresponds to future-conditional uses of the English word <a href="../../en/if.php"><em>if</em></a>, and complements the other Gaelic conditional-hypothetical subordinating conjunctions <a href="nan.php"><strong>nan</strong></a> and <a href="mura.php"><strong>mura</strong></a>.</p>
<hr/>
<p>Here are some canonical corpus examples from reported speech:</p>
<ul>
  <li><strong>“Nach eil càirdean is nàbaidhean [againn] a <mark>chuidicheas</mark> sinn <mark>ma thig</mark> èiginn oirnn?”</strong><br/> 
  <small class="text-muted">“Don’t we have family and neighbours who will help us if we get into trouble?” [#2 87]</small></li>
  <li><strong>“<mark>Ma thèid</mark> thu dhan oifis, <mark>thoir</mark> dhìot do bhonaid aodaich agus bi ga thoinneamh air a bheulaibh.”</strong><br/> 
  <small class="text-muted">“If you go into the office, take off your cap and spin it before him.” [#2 91]</small></li>
  <li><strong>“<mark>Ma dh’fhalbhas</mark> tu, ’ille, <mark>cha leig</mark> thu leas tilleadh idir.”</strong><br/> 
  <small class="text-muted">“If you leave, lad, you needn’t bother returning at all.” [#2 91]</small></li>
  <li><strong>“<mark>Cha toir</mark> mi gu lagh thu <mark>ma nì</mark> thu car obrach dhòmhsa.”</strong><br/> 
  <small class="text-muted">“I won’t take you to the police if you do a bit of work for me.” [#2 29]</small></li>
  <li><strong>“<mark>Bidh</mark> mi fada nad chomain <mark>ma thig</mark> thu còmhla rium gus am faigh mi air a’ chiste fhosgladh.”</strong><br/> 
  <small class="text-muted">“I will be much in your debt if you come along with me to get the chest open.” [#2 41]</small></li>
</ul>
<p><strong>ma is</strong> is usually contracted to <strong>mas</strong>.</p>
<ul>
  <li><strong>“<mark>Mas</mark> e sin a tha sibh ag iarraidh, <mark>cosgaidh</mark> e barrachd.”</strong><br/> 
  <small class="text-muted">“If that is what you want, it will cost more.” [#2 100]</small></li>
</ul>
<p>The coordination in the following example shows that <strong>ma</strong> contains a relative pronoun <strong>a</strong>:</p>
<ul>
  <li><strong>“<mark>Ma bhios</mark> caraidean againn gu diathad ’s <mark>a bhios</mark> mi ’m meadhan fealla-dhà innse, <mark>èighidh</mark> i rium an salann a chur thuice ged a tha salann eile ri taobh ...”</strong><br> 
  <small class="text-muted">“If we have friends to lunch and I am telling a joke, she will shout at me to pass her the salt even though there is other salt beside her ...” [#2 104-5]</small></li>
</ul>
<p><strong>ma tha</strong> is often found as an alternative to <strong>ma bhios</strong>:</p>
<ul>
  <li><strong>“<mark>Ma tha</mark> e marbh, <mark>càit a bheil</mark> an corp?”</strong><br/> 
  <small class="text-muted">“If he is dead, where is the body?” [#2 62]</small></li>
  <li><strong>“<mark>Tha</mark> thu air do mhealladh <mark>ma tha</mark> thu a’ smaoineachadh gu bheil thu san arm fhathast, a Chaiptein Chaimbeil.”</strong><br/>
  <small class="text-muted">“You are deceiving yourself if you think that you are still in the army, Captain Campbell.” [#2 92]</small></li>
  <li><strong>“Bruidhinn ri do mhàthair, agus <mark>ma tha</mark> i deònach, <mark>faodaidh</mark> tu tòiseachadh a-màireach.”</strong><br/>
  <small class="text-muted">“Speak to your mother, and if she is willing, you can start tomorrow.” [#2 12]</small></li>
</ul>
<p>Here are some non-canonical examples from reported speech:</p>
<ul>
  <li><strong>“Tha mi cinnteach nach biodh diofar le mo charaid mar a thachair, gu h-àraidh <mark>ma bha</mark> pìobair aig an uaigh.”</strong><br/> 
  <small class="text-muted">“I am certain that it would make no different for my friend how it happened, especially if there was a piper at the graveside.” [#2 20]</small></li>
  <li><strong>“Sgrìobh mi thuice ’s thuirt mi gu <mark>robh</mark> i air a mealladh <mark>ma bha</mark> i a’ creidsinn gu robh luach sam bith ann an ciste a’ Chaiptein.”</strong><br/>
  <small class="text-muted">“I wrote to her and I said that she was deceiving herself if she believed that there was anything of value in the Captain”s chest.” [#2 40]</small></li>
</ul>
<p>Here are some non-canonical examples from past-tense narrative:</p>
<ul>
  <li><strong>Ged nach robh cuimhne agam air na litrichean a bha air a’ chàr dhearg, <mark>ma mhothaich</mark> mi dhaibh idir, bha deagh chuimhne agam air an àireamh.</strong><br/>
  <small class="text-muted">‘Although I don’t recall the letters that were on the red car, if I was aware of them at all, I remember the number well.’ [#2 50-1]</small></li>
  <li><strong>Ach <mark>ma bha</mark> e a’ smaoineachadh gun tug seo a’ ghaoth à seòl Sam Broom, <mark>bha</mark> e air a mhealladh.</strong><br/>
  <small class="text-muted">‘But if he was thinking that this would take the wind out of Sam Broom’s sails, he was mistaken.’ [#2 66]</small></li>
  <li><strong>“<mark>Dè</mark> ’m math a dhol gu poileas leis an sgeulachd <mark>ma bha</mark> oifigear às an Taigh-Chusbainn am pòcaid nan eucorach?”</strong><br/>
  <small class="text-muted">“What would be the point of going to the police with the story if a customs officer was in the pocket of the criminals?” [#2 118]</small></li>
  <li><strong>Ann an tiotadh thàinig Carol, agus <mark>ma ghabh</mark> ise iongnadh, <mark>’s</mark> e <mark>ghabh</mark> an t-iongnadh esan.</strong><br/> 
  <small class="text-muted">‘In a bit Carol came, and if she was surprised, he certainly was surprised.’ [#2 108]</strong></li>
</ul>

