<h1>ach</h1>
<p><strong>ach</strong> is a contrastive coordinating conjunction corresponding to English ‘but’.</p>
<hr/>
<p>Parataxis:</p>
<ul>
  <li><strong>Sheas Iain <mark>ach</mark> shuidh Anna.</strong> ‘John stood up but Anna sat down.’ [DM92 163]</li>
</ul>
<p>Hypotaxis/concession:</p>
<ul>
  <li><strong>Sheasadh Iain <mark>ach</mark> Anna suidhe.</strong> ‘John would stand up provided that Anna sat down.’ [DM92 164]</li>
</ul>


