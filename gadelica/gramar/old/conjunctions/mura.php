<h1>mura</h1>
<p><strong>mura</strong> is a <a href="../xx/subordinatingConjunctions.php">subordinating conjunction</a>, typically governing a <a href="../xx/tense.php">future or conditional</a> <a href="../xx/dependency.php">dependent</a> verb form, and usually modifying a verb form or particle with the same tense.</p>
<p><strong>mura</strong> corresponds to conditional uses of the English words <a href="../../en/if.php"><em>if (not)</em></a> and <a href="../en/unless.php"><em>unless</em></a>, 
and as such is the <a href="../xx/polarity.php">negative</a> counterpart to the Gaelic subordinating conjunctions <a href="ma.php"><strong>ma</strong></a> and <a href="nan.php"><strong>nan</strong></a>.</p>
<hr/>
<p>Here are some canonical corpus examples:</p>
<ul>
  <li><strong>“<mark>Mura dèan</mark> thu sin, <mark>chan</mark> e mise a <mark>thig</mark> a choimhead ort idir.”</strong><br/>
  <small class="text-muted">“If you don't do that, it certainly won’t be me who comes to see you.” [#2: 55]</small></li>
  <li><strong>“Tha e air gabhail cùmhnant eile agus <mark>cha choilean</mark> e i <mark>mura h-obraich</mark> sinn gu lèir seach-thìm airson trì mìosan.”</strong><br/>
  <small class="text-muted">“He has taken on another contract and he won’t fulfill it unless we all work overtime for three months.” [#2: 90]</small></li>
  <li><strong>“Tha mise a’ dol an-diugh air làithean-saora dhan Eipheit airson mìos, agus bidh na craobhan nam fiodh-connaidh agad dhomh nuair a thilleas mi. <mark>Mura bi</mark> ...” Tharraing e a mheur tarsainn air amhaich.</strong><br/>
  <small class="text-muted">‘“I am going on holiday today to Egypt for a month, and you will have turned the trees into firewood for me when I return. If not ...”. He pulled his finger across his throat.’ [#2: 30]</small></li>
</ul>
<p><strong>mura robh</strong> is sometimes found as an alternative to <strong>mura biodh</strong>:</p>
<ul>
  <li><strong><mark>Mura robh</mark> càil aca a bha iad a’ falach, <mark>carson nach leigeadh</mark> iad a-steach e?</strong><br/>
  <small class="text-muted">‘If they had nothing to hide, why would they not let him in?’ [#2: 66]</small></p>
</ul>
<p><strong>mura b’ e</strong> often behaves like a preposition</p>
<ul>
  <li><strong>Is iongantach gu robh am poileas air a lorg gu bràth <mark>mura b’ e</mark> aon rud neònach.</strong><br/> 
  <small class="text-muted">‘It is unlikely that the police would ever have found them if it were not for one strange thing.’ [#2 50]</small></li>
</ul>


