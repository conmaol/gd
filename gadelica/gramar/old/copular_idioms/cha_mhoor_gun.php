<h1>cha mhòr gun/nach ...</h1>
<p><strong>cha mhòr gun/nach ...</strong> is a <a href="../../xx/degreeExpressions.php">degree expression</a> equivalent to the English adverbs of degree English: ‘scarcely’, ‘hardly’, ‘nearly’, ‘almost’.</p>
<h4>cha mhòr gun ...</h4>
<ul>
  <li><strong><mark>Cha mhòr gun do mhothaich</mark> e i.</strong><br/>
  <small class="text-muted">‘He hardly noticed her.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr gun cuala iad mi.</mark> e i.</strong><br/>
  <small class="text-muted">‘They hardly heard me.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr gum bitheadh</mark>e ochd bliadhna a dh’aois.</strong><br/>
  <small class="text-muted">‘He would scarcely be eight years old.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr gun ruigeadh</mark>e air an sgeilp.</strong><br/>
  <small class="text-muted">‘He could hardly reach the shelf.’ [CM 438]</small></li>
</ul>
<h4>cha mhòr nach ...</h4>
<ul>
  <li><strong><mark>Cha mhòr nach robh</mark> na fiachan pàighte nuair a thàinig stoirm.</strong><br/>
  <small class="text-muted">‘The debts were nearly paid off when a storm came.’ [#2 94]</small></li>
  <li><strong><mark>Cha mhòr nach robh</mark> i cho àrd ris fhèin.</strong><br/>
  <small class="text-muted">‘She was nearly as tall as him.’ [#2 108]</small></li>
  <li><strong><mark>Cha mhòr nach fhaod</mark> mi a ràdh gu robh a’ chiste falamh.</strong><br/>
  <small class="text-muted">‘I may almost say that the chest was empty.’ [???] [#2 42]</small></li>
  <li><strong><mark>Cha mhòr nach do bhris</mark> e an uinneag.</strong><br/>
  <small class="text-muted">‘He almost broke the window.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr nach do chaill</mark> e a bheatha.</strong><br/>
  <small class="text-muted">‘He almost lost his life.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr nach fhaic</mark> e cho math san dorchadas is a chì e ri solas latha.</strong><br/>
  <small class="text-muted">‘He sees almost as well in the darkness as he sees in the daylight.’ [CM 438]</small></li>
  <li><strong><mark>Cha mhòr nach d’fhuair</mark> e e.</strong><br/>
  <small class="text-muted">‘He nearly got it.’ [CM 438]</small></li>
</ul>
<p>Other degree expressions in Gaelic are: <strong><a href="is_gann_gun.php">is gann gun</a></strong> ‘scarcely’, ‘hardly’, ‘almost not’.</p>
