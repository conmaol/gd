<h1>is gann gun ...</h1>
<p><strong>is gann gun ...</strong> is a <a href="../../xx/degreeExpressions.php">degree expression</a> equivalent to the English adverbs of degree ‘scarcely’, ‘hardly’, ‘almost not’.</p>
<ul>
  <li><strong>Thòisich an siorraidh ag èigheachd ainmean clann an ath chlas, ach <mark>’s gann gun cuala no gu faca</mark> mise càil tuilleadh.</strong><br/> 
  <small class="text-muted">‘The sheriff started calling out the names of children from the next class, but I could scarcely hear or see anything any more.’ [#2 71]</small></li>
  <li><strong><mark>Bu ghann gun creideadh</mark> Dan a shùilean.</strong><br/>
  <small class="text-muted">‘Dan could scarcely believe his eyes.’ [#2 118]</small></li>
  <li><strong><mark>Is gann gun robh</mark> gu leòr ann airson a h-uile duine.</strong><br/>
  <small class="text-muted">‘There was hardly enough for all.’ [CM 324]</small></li>
  <li><strong><mark>Is gann gun ruigeadh</mark> e air an sgeilp.</strong><br/>
  <small class="text-muted">‘He could scarcely reach the shelf.’ [CM 324]</small></li>
  <li><strong><mark>’S gann gun robh</mark> e air a h-ainmeachadh nuair a ...</strong><br/>
  <small class="text-muted">‘He had scarcely announced her when ...’ [CM 324]</small></li>
  <li><strong><mark>’S gann gun gabh</mark> e a chreidsinn ach ...</strong><br/>
  <small class="text-muted">‘It can scarcely be believed but ...’ [CM 324]</small></li>
</ul>
<p>The following example is non-canonical, cf. <em>Is gann gum fiach e ...</em>:</p>
<ul>
  <li><strong><mark>Is gann is</mark> fhiach e a bhiadhadh.</strong><br/>
  <small class="text-muted">‘He is scarcely worth his feeding.’ [CM 324]</small></li>
</ul>
<p>Other degree expressions in Gaelic are: <strong><a href="cha_mhoor_gun">cha mhòr gun/nach ...</a></strong> ‘scarcely’, ‘hardly’, ‘almost’, ‘nearly’.</p>
