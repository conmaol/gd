  <li><strong>Cha <mark>b’ fheàirrde na gillean gur e</mark> ceàrraiche a bh’ ann am Mgr. Grey.</strong><br/>
    <small class="text-muted">It did the boys no good that Mr Grey was a gambler. [#2 53]</small></li>
  <li><strong>Bha a phàrantan a’ smaoineachadh gum <mark>b’ fheàirrde e</mark> greis còmhla ri Seòras Mòr.</strong><br/>
    <small class="text-muted">‘His parents thought that time with Big George would do him good.’ [#2 78]</small></li>
  <li><strong>“Seo an seòrsa là a bh’ ann nuair a thuirt an ‘Auk’ rium gum <mark>b’ fheàirrde mi drama</mark>.”</strong><br/>
    <small class="text-muted">“It was this kind of day when the ‘Auk’ said to me that a dram would be good for me.” [#2 16]</small></li>
  <li><strong><mark>B’ fheàirrde sinn gun tàinig</mark> Elsie May.</strong><br/>
    <small class="text-muted">‘It did us good that Elsie May came.’ [#2 29]</small></li>
  <li><strong>“<mark>B’ fheàirrde mi bruidhinn</mark> riut.”</strong><br/>
