<h1>is fheudar do ...</h1>
<p>‘must’, ‘have to’, ‘need to’, ‘be necessary for’</p>
<ul>
  <li><strong><mark>B’ fheudar do Eilidh seo eadar-theangachadh</mark> do Newall.</strong><br/>
    <small class="text-muted">‘Helen had to translate this for Newall.’ [#2 80]</small></li>
  <li><strong><mark>B’ fheudar do Phàdraig a dhol</mark> a sheòladh.</strong><br/>
    <small class="text-muted">‘Peter had to go to sea.’ [#2 96]</small></li>
  <li><strong><mark>B’ fheudar dha na balaich falbh</mark> a sheòladh.</strong><br/>
    <small class="text-muted">‘The boys had to leave to go to sea.’ [#2 97]</small></li>
  <li><strong><mark>B’ fheudar do Aonghas an t-oilthigh fhàgail</mark> leis a’ chaitheamh.</strong><br/>
    <small class="text-muted">‘Angus had to leave the university with consumption.’ [#2 5-6]</small></li>
  <li><strong><mark>B’ fheudar dha an dùthaich fhàgail</mark>.</strong><br/>
    <small class="text-muted">‘He had to leave the country.’ [CM 297]</small></li>
  <li><strong><mark>B’ fheudar dhi togail</mark> a-mach a chosnadh airgid.</strong><br/>
    <small class="text-muted">‘She had to go out to earn money.’ [CM 297]</small></li>
</ul>
<p>Present tense examples:</p>
<ul>
  <li><strong><mark>Is fheudar dhomh an t-each a reic</mark>.</strong><br/>
    <small class="text-muted">‘I must sell the horse.’ [CM 297]</small></li>
</ul>
<p>An impersonal example:</p>
<ul>
  <li><strong><mark>B’ fheudar tuilleadh uidheim a thoirt</mark> bho Thorcall airson sgùilean ùra.</strong><br/>
    <small class="text-muted">‘It was necessary to take more equipment from Torquil for new sculls.’ [#2 94]</small></li>
</ul>
<p><strong>mas fheudar</strong> ‘if necessary’</p>
<ul>
  <li><strong>Bheir mi seòladh dhut <mark>mas fheudar</mark>.</strong><br/>
    <small class="text-muted">‘I’ll give you directions if need be.’ [CM 297]</small></li>
</ul>
<p><strong>is fheudar gun ...</strong> – epistemic modality</p>
<ul>
  <li><strong><mark>’S fheudar gun aithnich</mark> thu e.</strong><br/>
    <small class="text-muted">‘You must know him.’ [CM 297]</small></li>
</ul>


