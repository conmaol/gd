<h1>cha</h1>
<p><strong>cha</strong> is the ungoverned negative declarative <a href="../../xx/verbParticles.php">verb particle</a>, contrasting with the other negative particle <strong><a href="nach.php">nach</a></strong>.</p>
<p><strong>cha</strong> lenites a following verb or adjective.</p>
<p><strong>cha</strong> is written as <strong>chan</strong> when the following word starts with a vowel (including <strong>fh-</strong>).</p>
<hr/>
<p>Examples from reported speech in past-tense narrative:</p>
<ul>
  <li><strong>“<mark>Cha mhothaicheadh</mark> tu dha air an t-sràid,”</strong><br/>
  <small class="text-muted"> “You wouldn’t notice him on the street.” [#2 60]</small></li>
  <li><strong>“<mark>Cha shalaich</mark> mise mo làmhan air nì cho suarach riut.”</strong><br/>
  <small class="text-muted">“I will not soil my hands on something as worthless as you.” [#2 92]</small></li>
  <li><strong>“Mar tha fios agad, <mark>cha do rinn</mark> mi sin.”</strong><br/>
  <small class="text-muted">“As you know, I didn’t do that.” [#2 109]</small></li>
  <li><strong>“Mar sin <mark>cha robh</mark> feum aice air biadh.”</strong><br/>
  <small class="text-muted">“Therefore, she did not need food.” [#2 63]</small></li>
</ul>
<p>With the present tense of <strong>bi</strong>, <strong>chan eil</strong> is used rather than ☠️‘cha bheil’:</p>
<ul>
  <li><strong>“<mark>Chan eil</mark> sibh a’ dol a chreidsinn seo.”</strong><br/>
  <small class="text-muted">“You are not going to believe this.” [#2 48]</small></li>
  <li><strong>“<mark>Chan eil</mark> càil a choltas uilebheist air.”</strong><br/>
  <small class="text-muted">“He doesn’t look like a monster.” [#2 60]</small></li>
</ul>
<p>Copula examples:</p>
<ul>
  <li><strong>“<mark>Chan e</mark> àm airson fealla-dhà a tha seo idir.”</strong><br/>
  <small class="text-muted">“This is certainly not the time for joking.” [#2 56]</small></li>
  <li><strong>“<mark>Chan ann</mark> airson biadh a tha siorc a’ toirt ionnsaigh air duine idir.”</strong><br/>
  <small class="text-muted">“A shark certainly does not attack a human for food.” [#2 63]</small></li>
</ul>
