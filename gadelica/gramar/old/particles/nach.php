<h1>nach</h1>
<p><strong>nach</strong> is a negative <a href="../../xx/verbParticles.php">verb particle</a>, contrasting with the ungoverned negative declarative particle <strong><a href="cha.php">cha</a></strong>, the positive interrogative particle <strong><a href="an.php">an</a></strong>, and with the governed positive declarative particle <strong><a href="gun.php">gun</a></strong>.</p>
<p><strong>nach</strong> can be either ungoverned (introducing a matrix question), or governed by another word (introducing an embedded question, statement or relative clause as part of a compound conjunction).</p>
<hr/>
