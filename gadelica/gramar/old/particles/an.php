<p>lorg</p>
<ul>
  <li>Thuirt Torcall gu robh prìs nan giomach air a dhol sìos, agus cha robh dòigh aig na gillean air <mark>lorg an</mark> e an fhìrinn a bha seo.  [#2 95]</li>
</ul>
<p>innis</p>
<ul>
  <li>Bha bràthair aige ann an Lunnainn ag obair ann am margaid an èisg agus dh’innseadh e dhaibh prìs nan giomach, agus an robh feadhainn marbh anns na bucais aca. [#2 95]</li>
  <li>“Innis dhomh an tug e bradan dhachaigh.” Tell me if he brought a salmon home. [#2 113]</li>
</ul>
<p>seall</p>
<ul>
  <li>“Nach bu chòir dhuit a dhol a shealltainn a bheil d’ athair a’ tighinn,” ars ise. [#2 123]</li>
  <li>Chaidh sinn timcheall an taighe a shealltainn an robh doras no uinneag fosgailte. [#2 14]</li>
</ul>
<p>beachdaich</p>
<ul>
  <li>Ach mus cuir sibh cus coire orm, beachdaichibh an robh sibh air an ceum sin a dhèanamh. [#2 13]</li>
</ul>
<p>dùil</p>
<ul>
  <li><strong>“’N <mark>dùil a</mark> bheil fios aig a’ bhodach mar a tha mi air mo leòn?”</strong><br/>
  <small class="text-muted">“I wonder if the old man knoes how I have been hurt?” [#2 125]</small></li>
  <li><strong>“Tha an àireamh ceart ach ’s e an t-sràid cheàrr a th’ ann. An <mark>dùil am</mark> b’ urrainn dhuibh ... ?”</strong><br/>
  <small class="text-muted">“The number is right but it’s the wrong street. I wonder if you could ... ?” [#2 14]</small></li>
</ul>
<p>feuch</p>
<ul>
  <li><strong>Bha athair air cluinntinn gu robh eathar ag iasgach a-mach bho chladach Ealasword, agus chaidh e a <mark>dh’fheuchainn am</mark> faigheadh e iasg.</strong><br/>
  <small class="text-muted">‘His father had heard that a boat was fishing out of Ealasword, and he went to try (and see) if he could get fish.’ [#2 123]</small></li>
</ul>
<h4>Other governed uses</h4>
<h5>càite an</h5>
<ul>
  <li><strong>“Ma tha e marbh, <mark>càit a bheil</mark> an corp?”</strong><br/>
  <small class="text-muted">“If he is dead, where is the body?” [#2 62]</small></li>
  <li><strong>Bha e ag ràdh gur ann airson coimhead <mark>càit an laigheadh</mark> na geòidh a bha e.</strong><br/>
  <small class="text-muted">‘He was saying that he was wanting to see where the geese laid their eggs.’ [#2 79]</small></li>
</ul>
<h5>far an</h5>
<ul>
  <li><strong>Bha sinn fhathast <mark>far na thog</mark> mi e, faisg air taigh beag le càr dearg aig a cheann.</strong><br/>
  <small class="text-muted">‘We were still where I picked him up, near a little house with a red car in front.’ [#2 49]</small></li>
</ul>
<h5>gus an</h5>
<ul>
  <li><strong>“’S e a dh’fheumas mi àite anns am fuirich mi airson là no dhà <mark>gus an tig</mark> airgead bhom athair.”</strong><br/>
  <small class="text-muted">“What I need is a place to stay for a couple of days until money arrived from my father.” [#2 48]</small></li>
  <li><strong>“Bheil thu airson gu stad mi aig a’ chiad fòn <mark>gus an innis</mark> thu seo dhan phoileas?”</strong><br/>
  <small class="text-muted">“Do you want me to stop at the first phone (box) so that you can tell this to the police?” [#2 48]</small></li>
</ul>
<h5>preposition + an</h5>
<ul>
  <li><strong>Lorg iad an càr beag dearg aig an taigh <mark>aig na thog</mark> mi Brian.</strong><br/>
  <small class="text-muted">‘They found the little red car at the house at which I (had) picked up Brian.’ [#2 51]</small></li>
</ul>








