<h1>an</h1>
<p><strong>an</strong> is the positive interrogative <a href="../../xx/verbParticles.php">verb particle</a>, contrasting with the negative interrogative <strong><a href="nach.php">nach</a></strong>.</p>
<p><strong>an</strong> is written as <strong>am</strong> when the following word starts with a labial consonant <strong>b-</strong>, <strong>p-</strong>, <strong>f-</strong>, <strong>m-</strong>.</p>
<p><strong>an</strong> can be either ungoverned (introducing a matrix question), or governed by another word (introducing an embedded question or as part of a compound conjunction).</p>
<hr/>
<h3>1. Ungoverned <em>an</em></h3>
<p>When <strong>an</strong> is not governed by another word (or is governed by a coordinating conjunction), it introduces a positive matrix question.</p>
<p>Here are some corpus examples involving reported speech in narrative:</p>
<ul>
  <li><strong>“<mark>An d’ fhuair</mark> esan dealachadh ... ?”</strong><br/>
  <small class="text-muted">“Did he get a divorce ...?” [#2 105]</small></li>
  <li><strong>“<mark>An innseadh</mark> fear-meallaidh ainm ’s a sheòladh?”</strong><br/>
  <small class="text-muted">“Would a fraudster give his name and address?” [#2 116]</small></li>
</ul>
<p>The old form <strong>am beil</strong> is nowadays almost always reduced to <strong>a bheil</strong>:</p>
<ul>
  <li><strong>“<mark>A bheil</mark> thu a’ tuigsinn gu bheil thu dol a’ bhàsachadh?”</strong><br/>
  <small class="text-muted">“Do you understand that you are going to die?” [#2 6]</small></li>
  <li><strong>“<mark>A bheil</mark> ùidh agad ann an gàirnealaireachd?”</strong><br/>
  <small class="text-muted">“Are you interested in gardening?” [#2 12]</small></li>
</ul>
<p><strong>an do</strong> is sometimes reduced to just <strong>na</strong>:</p>
<ul>
  <li><strong>“<mark>Na sheall</mark> thu a bheil duine anns an taigh sin?”</strong><br/>
  <small class="text-muted">“Did you see if someone was in that house? [#2 49]</small></li>
  <li><strong>“Bha am ministear a-staigh cuideachd. <mark>Na rinn</mark> thu sgrùdadh mar seo airsan?”</strong><br/>
  <small class="text-muted">“The minister was in as well. Did you interrogate him like this too?” [#2 22]</small></li>
</ul>
<p><strong>an is</strong> is always reduced to just <strong>an</strong> (or even just <strong>’n</strong>):</p>
<ul>
  <li><strong>“<mark>’N</mark> ann a’ feuchainn ri muncaidh a dhèanamh dhìomsa tha sibh?”</strong><br/>
  <small class="text-muted">“Are you trying to make a fool of me?” [#2 67]</small></li>
  <li><strong>“<mark>’N</mark> urrainn dhut tòiseachadh a-màireach?”</strong><br/>
  <small class="text-muted">“Are you able to start tomorrow?” [#2 10]</small></li>
</ul>
<hr/>
<h3>2. Governed <em>an</em></h3>
<h4>Embedded questions</h4>
<p>In the following examples, <strong>an</strong> is governed by the verb <strong>faighnich</strong> ‘ask (if)’</p>
<ul>
  <li><strong><mark>Dh’fhaighnich</mark> e <mark>an</mark> robh Magaidh a-staigh.</strong><br/>
  <small class="text-muted">‘He asked if Maggie was in.’ [#2 124]</small></li>  
  <li><strong>Thàinig Aonghas tarsainn air an leabhran ann an trèana, agus bha e a’ <mark>faighneachd an</mark> aithnicheadh Iain iad.</strong><br/> 
  <small class="text-muted">‘Angus came across the booklet on a train, and he was asking if John recognised them.’ [#2 86]</small></li>
  <li><strong>Nuair a <mark>dh’fhaighnich</mark> e do Mhata <mark>an</mark> e siùil a bha iad ag iarraidh ’s e a thuirt e:</strong><br/>
  <small class="text-muted">‘When he asked Matthew if it was sails that he wanted what he said was:’ [#2 99]</small></li>
  <li><strong><mark>Dh’fhaighnich</mark> fear aca dhomh <mark>na</mark> mhothaich mi an robh miotagan air na daoine.</strong><br/> 
  <small class="text-muted">‘One of them asked me if I had noticed if the men had gloves on.’ [#2 15]</small></li>
</ul>
