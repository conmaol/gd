<ul>
  <li><strong>Dh’fhaighnich fear aca dhomh na <mark>mhothaich</mark> mi <mark>an</mark> robh miotagan air na daoine.</strong><br/> 
  <small class="text-muted">‘One of them asked me if I had noticed if the men had gloves on.’ [#2 15]</small></li>
