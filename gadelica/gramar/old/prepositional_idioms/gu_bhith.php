<h1>gu bhith</h1>
<p><strong>gu bhith</strong> is a <a href="../../xx/degreeExpressions.php">degree expression</a> corresponding to the English degree adverbs ‘almost’, ‘nearly’, ‘on the point of being’.</p>
<p><strong>gu bhith</strong> usually governs a (predicative) adjective and is itself usually governed by a form of <strong><a href="../verbs/bi.php">bi</a></strong>.</p>
<hr/>
<p>Basic examples:</p>
<ul>
  <li><strong>Tha mi <mark>gu bhith deiseil</mark>.</strong><br/> 
  <small class="text-muted">‘I am almost ready.’ [CM 347]</small></li>
</ul>
<p>Examples with <strong><a href="../particles/cho.php">cho</a></strong>:</p>
<ul>
  <li><strong>Ach cha b’ fhada gus na thuig e nach robh a’ chùis <mark>gu bhith cho soirbh</mark> ri sin idir.</strong><br/> 
  <small class="text-muted">‘But it was not long until he understood that the matter wasn’t nearly as easy as that at all.’ [#2 76]</small></li>
</ul>
<p><strong>gus a bhith</strong> is sometimes used as an alternative:</p>
<ul>
  <li><strong>“Ach tha là thrustairean dhed leithid <mark>gus a bhith seachad</mark> ...”</strong><br/>
  <small class="text-muted">“But the days of bastards like you are almost over.” [#2 92]</small></li>
</ul>
<p>Example with a prepositional phrase:</p>
<ul>
  <li><strong>’S ann nuair a bha athair <mark>gus a bhith aige</mark> a thuig Dan gu robh am bodach gus a bheul a bhualadh fodha.</strong><br/>
  <small>‘It was when his father was almost at him that Dan understood that the old man was ...’ ??? [#2 123]</small></li>
</ul>
<p>MM: this is a specialisation of the gu + VN construction.</p>
<p>cf. <strong><a href="../prepositions/gu.php">gu</a></strong>, <strong><a href="../verbs/bi.php">bi</a></strong></p>
