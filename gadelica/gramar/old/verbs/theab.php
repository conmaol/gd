<h1>theab</h1>
<p><strong>theab</strong> is a past tense <a href="../../xx/defectiveVerbs.php">defective verb</a>, and a <a href="../../xx/degreeExpressions.php">degree expression</a>, corresponding to the English degree adverbs ‘almost’, ‘nearly’.</p>
<hr/>
<p>Some simple examples:</p>
<ul>
  <li><strong>“<mark>Theab thu mis’ a chrochadh</mark> airson do gheall.”</strong><br/>
  <small>“You nearly hanged me for the sake of your wager.” [#2 58]</small></li>
  <li><strong><mark>Theab mi tuiteam</mark>.</strong><br/>
  <small>‘I almost fell.’ [CM 577]</small></li>
  <li><strong><mark>An do theab e tuiteam</mark>?.</strong><br/>
  <small>‘Did he almost fall?’ [CM 577]</small></li>
  <li><strong><mark>Theab e bàsachadh</mark>.</strong><br/>
  <small>‘He almost died.’ [CM 577]</small></li>
  <li><strong>O, <mark>theab mi dìochuimhneachadh</mark>.</strong><br/>
  <small>‘Oh, I almost forgot.’ [CM 577]</small></li>
  <li><strong><mark>Theab thu mo bhualadh</mark>.</strong><br/>
  <small>‘You almost hit me.’ [CM 577]</small></li>
  <li><strong><mark>Theab mi dhol</mark> ann.</strong><br/>
  <small>‘I almost went (there)’ [CM 577]</small></li>
  <li><strong>... far <mark>na theab i dhol</mark> air na creagan ...</strong><br/>
  <small>‘... where she (the boat) almost went onto the rocks ....’ [CM 577]</small></li>
  <li><strong><mark>Theab i an uinneag a bhriseadh</mark>.</strong><br/>
  <small>‘She almost broke the window.’ [CM 577]</small></li>
  <li><strong><mark>Theab mi buille a thoirt</mark> dha an clàr an aodainn.</strong><br/>
  <small>‘I almost punched him right in the face.’ [CM 577]</small></li>
</ul>
<p>Negative example:</p>
<ul>
  <li><strong><mark>Theab mi gun a dhol</mark> ann idir.</strong><br/>
  <small>‘I almost didn’t go at all.’ [CM 577]</small></li>
</ul>
<p>Odd double negative:</p>
<ul>
  <li><strong><mark>Theab nach deach</mark> e dhachaidh gun chas bhriste.</strong><br/>
  <small>‘He almost went home with a broken leg.’ [CM 577-8]</small></li>
</ul>
<p>Passive examples:</p>
<ul>
  <li><strong><mark>Theabas an glachadh</mark> uair no dhà.</strong><br/>
  <small>‘They were almost caught once or twice.’, lit. ‘almost was done their catching’ [CM 578]</small></li>
  <li><strong><mark>An do theabas an glachadh</mark>?</strong><br/>
  <small>‘Were they almost caught?’ [CM 578]</small></li>
  <li><strong><mark>Theabadh a bhàthadh</mark>.</strong><br/>
  <small>‘He was almost drowned.’ [CM 578]</small></li>
  <li><strong>... far <mark>an do theabadh am faicinn</mark> ...</strong><br/>
  <small>‘... where they were almost seen ...’ [CM 577]</small></li>
</ul>
<p>A proverb:</p>
<ul>
  <li><strong>Cha do rinn ‘theab’ cron riamh.</strong><br/>
  <small>‘Almost’ never did any harm. [CM 578]</small></li>
</ul>

