<h1>thoir</h1>
<p><strong>thoir</strong> is an <a href="../../xx/irregularVerbs.php">irregular verb</a> corresponding to the English ‘take’, ‘bring’, ‘give’.</p>
<p>The forms of <strong>thoir</strong> are:</p>
