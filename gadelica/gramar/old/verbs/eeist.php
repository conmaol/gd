<h1>èist</h1>
<p>cf. <strong><a href="cluinn.php">cluinn</a></strong></p>
<hr/>
<p><strong>èist</strong> – ‘Listen’, ‘pay attention’, ‘take heed’, ‘concentrate’, ‘focus’</p>
<ul>
  <li><strong>“<mark>Eist thusa</mark> agus chì thu gur e an aon dòigh a nì thu a’ chùis air Wallace.”</strong><br/> 
    <small class="text-muted">“If you listen, you’ll see that it’s the only way you’ll get the better of Wallace.” [#2 56]</small></li>
  <li><strong>Bha Uisdean ag <mark>èisteachd</mark> le iongantas.</strong><br/>
    <small class="text-muted">‘Hugh was listening in surprise.’ [#2 56]</small></li>
</ul>
<p><strong>èist ri duine</strong> – ‘listen to someone’, ‘pay attention to someone’, ‘concentrate/focus on what someone is saying’</p>
<ul>
  <li><strong>Ach chan <mark>èisteadh Rob riutha</mark>.</strong><br/> 
    <small class="text-muted">‘But Rob  wouldn’t listen to them.’ [#2 6]</small></li>
  <li><strong>“<mark>Eist rium</mark>, Carol.”</strong><br/> 
    <small class="text-muted">“Listen to me, Carol.” [#2 109]</small></li>
<li><strong>“<mark>Eist thusa riumsa</mark>, ’ille.”</strong><br/> 
    <small class="text-muted">“You listen to me, boy.” [#2 22]</small></li>
</ul>
<p><strong>èist ri rud</strong> – ‘listen to something’, ‘pay attention to something’, ‘concentrate/focus on something’</p>
<ul>
  <li><strong>Cha robh Dan a-riamh deònach <mark>èisteachd ri comhairle</mark> bho phàrantan mu dheidhinn leannanachd.</strong><br/> 
    <small class="text-muted">‘Dan was never willing to listen to advice from his parents about courtship.’ [#2 125]</small></li>
</ul>


