<h1>seall</h1>
<p><strong>seall</strong> is a <a href="../../xx/regularVerbs.php">regular verb</a> of visual attention corresponding to the English ‘look’, ‘show’.
It is closely related to the other visual verbs <strong><a href="faic.php">faic</a></strong> ‘see’, <strong><a href="coimhead.php">coimhead</a></strong> ‘look’, <strong><a href="mothaich.php">mothaich</a></strong> ‘notice’,
and the idiom <strong><a href="../nouns/suuil.php#thoir_suuil_air">thoir sùil (air)</a></strong> ‘look (at)’.
It is also closely related to the verb of auditory attention <strong><a href="eeist.php">èist</a></strong> ‘listen’.</p>
<p>The verbal noun form of <strong>seall</strong> is <strong>sealltainn</strong>.</p>
<p>The main uses of <strong>seall</strong> are:</p>
<ul>
  <li>intransitive
    <ul>
      <li><a href="#ri">with preposition <em>ri</em></a> e.g. <em>seall ri duine/rud</em></li>
      <li><a href="#path">with a path expression</a> e.g. <em>seall tro ...</em></li>  
    </ul>
  </li>
  <li>transitive
    <ul>
      <li><a href="#do">with preposition <em>do</em></a> e.g. <em>seall rud do dhuine</em></li>
    </ul>
  </li>
</ul>
<hr/>
<h3 id="intransitive">Intransitive uses – ‘look (at)’</h3>
<p id="ri">When used intransitively, <strong>seall</strong> usually means ‘look (at)’, with the object or person under observation being introduced by the preposition <strong><a href="../prepositions/ri.php">ri</a></strong>,
i.e. <strong>seall ri rud/duine</strong>:</p>
<ul>
  <li><strong>“<mark>Seall ri cas</mark> a’ bheathaich, ma-tà.”</strong><br/>
    <small class="text-muted">“Look at the animal’s foot, then.” [#2 58]</small></li>
  <li><strong>Nuair a <mark>sheall Sam ris an dealbh</mark>, chunnaic e gur e a dhealbh fhèin a bh’ ann.</strong><br/>
    <small class="text-muted">‘When Sam looked at the photograph, he saw that it was of himself.’ [#2 66]</small></li>
  <li><strong><mark>Sheall Diana gu feargach ri Màiri</mark>.</strong><br/>
    <small class="text-muted">‘Diana looked angrily at Mary.’ [#2 42]</small></li>
</ul>
<p id="path"><strong>seall</strong> can also be used with a <a href="../../xx/pathExpressions.php">path expression</a> in the sense of directing one’s gaze along a trajectory:</p>
<ul>
  <li><strong><mark>Sheall Eilidh tron phrosbaig</mark>.</strong><br/>
    <small class="text-muted">‘Helen looked through the telescope.’ [#2 81]</small></li>
  <li><strong>“<mark>Seall a-mach air an uinneig</mark>.”</strong><br/>
    <small class="text-muted">“Look out the window.” [#2 27]</small></li>
</ul>
<p id=""><strong>seall</strong> can take an embedded direct question, in the sense of ‘check if’, ‘(look and) see if’:</p>
<ul>
  <li><strong>“Na <mark>sheall thu a bheil</mark> duine anns an taigh sin?”</strong><br/>
    <small class="text-muted">“Did you check if anyone is in that house?” [#2 49]</small></li>
  <li><strong>“Nach bu chòir dhuit a dhol a <mark>shealltainn a bheil</mark> d’ athair a’ tighinn?”</strong><br/>
    <small class="text-muted">“Shouldn’t you go and check if your father is coming?” [#2 123]</small></li>
  <li><strong>Chaidh sinn timcheall an taighe a <mark>shealltainn an robh</mark> doras no uinneag fosgailte.</strong><br/>
    <small class="text-muted">‘We went around the house to check if a door or window was open.’ [#2 14]</small></li>
</ul>
<p>The following example appears to involve something like an embedded constituent question, cf. <strong>seall ciamar a</strong> ‘see how’:</p>
<ul>
  <li><strong>“<mark>Seall a-nis mar a tha</mark> am bùrn a’ dol sìos.”</strong><br/>
    <small class="text-muted">“Now see how the water is going down.” [#2 67]</small></li>
</ul>
<p>Finally, <strong>seall</strong> is occasionally used as a strict intransitive, with no other arguments apart from the subject:</p>
<ul>
  <li><strong>“Bheil sibh a’ creidsinn ann an gaol a’ chiad seallaidh?” “Tha, ach bu chaomh leam <mark>sealltainn</mark> a-rithist.”</strong><br/>
  <small class="text-muted">“Do you believe in love at first sight?” “Yes, but I’d want to take another look.” [#2 49]</small></li>
</ul>
<p>Note the following idiomatic usage:</p>
<ul>
  <li><strong>Mar as àbhaist bha mi a’ coimhead às dèidh an àite a cheart cho cùramach ’s a <mark>sheallainn às dèidh mo dhachaigh fhìn</mark>.</strong><br/>
  <small class="text-muted">‘As usual I was looking after the place just as diligently as I would look after my own home.’ [#2 13]</small></li>
</ul>
<hr/>
<h3>Transitive uses – ‘show’</h3>
<p id="do">When used as a transitive verb, <strong>seall</strong> has the meaning ‘show someone something’ and the indirect object is introduced by the preposition <strong><a href="../prepositions/do.php">do</a></strong>, 
i.e. <strong>seall rud do dhuine</strong> – ‘show someone something’:</p>
<ul>
  <li><strong><mark>Sheall am poileasman dealbh Mhangain do Ruairidh Beag</mark>.</strong><br/>
  <small class="text-muted">‘The policeman showed Mangan’s picture to Little Roddy.’ [#2 84]</small></li>
  <li><strong><mark>Sheall i dhomh clòsaid mhòr</mark> làn de dheilbh.</strong><br/>
  <small class="text-muted">‘She showed me a large closet full of pictures.’ [#2 8]</small></li>
  <li><strong>Nuair a thàinig Donnchadh a-nuas <mark>sheall e na lorg e do Mhòr</mark>.</strong><br/>
  <small class="text-muted">‘When Duncan came down, he showed Moira what he had found.’ [#2 22-3]</small></li>
</ul>
<p>Note that transitive <strong>seall</strong> can also be used in the progressive:</p>
<ul>
  <li><strong>Bha am ministear a-staigh am feasgar a thàinig am parsail, agus bha a’ bhantrach <mark>a’ sealltainn dha na fhuair i</mark> nuair a thàinig Frank a-steach.</strong><br/>
  <small class="text-muted">‘The minister was visiting the evening that the package came, and the widow was showing him what she had got when Frank came in.’ [#2 21]</small></li>
</ul>
<p><strong>seall do</strong> can also be used pseudo-transitively with an embedded declarative clause, i.e. <strong>seall do dhuine gu (bheil)</strong>:</p>
<ul>
  <li><strong>Nuair a chaidh mi a <mark>shealltainn dha mo charaid gu robh</mark> e agam cha robh Aonghas ann.</strong><br/>
  <small class="text-muted">‘When I went to show my friend that I had it, Angus was not there.’ [#2 35]</small></li>
</ul>
<p>The following uses appear to involve something like embedded constituent questions:</p>
<ul>
  <li><strong><mark>Sheall e dhaibh far an robh</mark> biadh is deoch sa chèaban.</strong><br/>
  <small class="text-muted">‘He shows them where the food and drink was in the cabin.’ [#2 100]</small></li>
  <li><strong>Chaidh fear a chur còmhla ris a <mark>shealltainn dha far an robh</mark> an obair.</strong><br/>
  <small class="text-muted">‘A man was sent with him to show him where the work was.’ [#2 10]</small></li>
  <li><strong>Nuair a bhiodh là blàth ann bhiodh sinn a-muigh san iodhlainn agus <mark>shealladh e dhomh mar a dhèanainn</mark> fuaim binn le bileag feòir,</strong><br/>
  <small class="text-muted">‘When it was warm, we would be outside in the yard and he would show me how to make a sweet sound with a blade of grass.’ [#2 33]</small></li>
</ul>
<p>It may well be the case that these uses are misunderstandings of constructions like <strong>seall do dhuine <mark>càite</mark> an ...</strong> and <strong>seall do dhuine <mark>ciamar</mark> a ...</strong>.</p>
<hr/>
<p>Notes:</p>
<ul>
  <li>Intransitive <strong>sealltainn ri</strong> does not appear to be used in the progressive or intentional constructions, with <strong><a href="coimhead.php">bi a’ coimhead ri</a></strong> and <strong><a href="coimhead.php">rach/thig a choimhead ri</a></strong> being preferred instead.</li>
  <li>Intransitive <strong>seall</strong> can appear in the imperative and hence can be seen as a volitional version of <strong><a href="faic.php">faic</a></strong> ‘see’.</li>
  <li>Transitive <strong>seall</strong> functions as the causative of intransitive <strong>seall</strong>, i.e. ‘show’ = ‘make look/see’.</li>
</ul>

