<h1>coimhead</h1>
<p><strong>coimhead</strong> is a <a href="../../xx/regularVerbs.php">regular verb</a> of visual attention/perception corresponding to the English ‘look, see’.
It is closely related to the other visual verbs <strong><a href="faic.php">faic</a></strong> ‘see’, <strong><a href="seall.php">seall</a></strong> ‘look, show’, <strong><a href="mothaich.php">mothaich</a></strong> ‘notice’,
and the idiom <strong><a href="../nouns/suuil.php#thoir_suuil_air">thoir sùil (air)</a></strong> ‘look (at)’.
It is also closely related to the verb of auditory attention <strong><a href="eeist.php">èist</a></strong> ‘listen’.</p>
<p>The verbal noun form of <strong>coimhead</strong> is <strong>coimhead</strong>, and this appears to be by far the most common form of the verb.</p>
<hr/>
<h3>Intransitive uses</h3>
<p><strong>coimhead</strong> is most commonly used as an intransitive verb, in a range of constructions.</p>
<h5>bi a’ coimhead ri</h5>
<p>This construction is the progressive variant of <strong><a href="seall.php">seall ri</a></strong> – ‘(be) looking at something/one’:</p>
<ul>
  <li><strong>Chuir na leugh Syd na thuaineal e. Bha inntinn bàn a’ <mark>coimhead ris an litir</mark>.</strong><br/>
    <small class="text-muted">‘What Syd read stupified him. An empty mind was looking at the letter.’ [#2 59]</small></li>
  <li><strong>Nuair a chunnaic mi a dhealbh air an teilidh, ’s ann <mark>ri aghaidh an duine a mharbh mi a bha mi coimhead</mark>.</strong><br/>
    <small class="text-muted">‘When I saw his picture on the telly, I was looking at the face of the man I had killed.’ [#2 63]</small></li>
  <li><strong>Thog e catalog agus thòisich e a’ leigeil air gur ann a’ <mark>coimhead ris</mark> a bha sinn.</strong><br/>
    <small class="text-muted">‘He picked up a catalogue and started to pretend that we were looking at it.’ [#2 32]</small></li>
</ul>
<h5>rach/thig a choimhead air</h5>
<p><strong>coimhead</strong> is also used frequently in intentional constructions, with the preposition <strong><a href="../prepositions/air.php">air</a></strong>.
When the complement is a person, the meaning is something like ‘go/come to visit/see somebody’</p>
<ul>
  <li><strong>An ceann seachdaine <mark>thàinig lorg-phoileas a choimhead orm</mark>.</strong><br/>
    <small class="text-muted">‘A week later a detective came to see me.’ [#2 116]</small></li>
  <li><strong>Am feasgar seo bha e mì-fhoighidneach, oir bha e a’ <mark>dol a choimhead air a leannan</mark> sa bhaile mhòr.</strong><br/>
    <small class="text-muted">‘This evening he was impatient, because he was going to see his girlfriend in the city.’ [#2 123]</small></li>
  <li><strong><mark>Thàinig Sir Claude Auchinlech dhan ospadal a choimhead air</mark>.</strong><br/>
    <small class="text-muted">‘Sir Claude Auchinlech came came to the hospital to see him.’ [#2 16]</small></li>
</ul>
<p>The following use of the progressive is very close related to this:</p>
<ul>
  <li><strong>an dèidh dha a <mark>bhith a’ coimhead air a bhean</mark> san ospadal</strong><br/>
    <small class="text-muted">‘after he had been to visit his wife in hospital’ [#2 117]</small></li>
</ul>
<p>When the complement is a thing, it means more like ‘go to look at’:</p>
<ul>
  <li><strong><mark>Chaidh iad an sin a choimhead air na craobhan</mark>.</strong><br/>
    <small class="text-muted">‘They went there to look at the trees.’ [#2 30]</small></li>
  <li><strong>‘Chan eil cead againn a <mark>dhol a choimhead air a’ chiste</mark>.’</strong><br/>
    <small class="text-muted">‘We weren’t allowed to go and look at the chest.’ [#2 38]</small></li>
</ul>
<h5>bi a’ coimhead air rud mar ...</h5>
<p>This construction means something like ‘regard/see something as’:</p>
<ul>
  <li><strong>Bha sinne a’ <mark>coimhead air mar aon dhe na prìomh bheusan</mark>!</strong><br/>
    <small class="text-muted">‘We regarded it [poaching] as one of the primary virtues!’ [#2 112]</small></li>
</ul>
<h5>bi a’ coimhead + adjective</h5>
<p>This construction describes the appearance of the subject in some way:</p>
<ul>
  <li><strong>Bha e duilich a chreidsinn gun rinn duine a bha <mark>coimhead cho stòlda</mark> an rud uabhasach a rinn e.</strong><br/>
    <small class="text-muted">‘It was hard to believe that someone who looked so staid had done the terrible thing which he did.’ [#2 61]</small></li>
  <li><strong>... balach eireachdail nach robh <mark>cho beag-lochd ’s a bha e a’ coimhead</mark>.</strong><br/>
    <small class="text-muted">... a handsome boy who was not as harmless as he looked. [#2 69-70]</small></li>
  <li><strong>“Tha ’n t-àite seo <mark>coimhead glè fhallain</mark>.”</strong><br/>
    <small class="text-muted">“This place looks very healthy.” [#2 17]</small></li>
</ul>
<h5>Idiomatic uses</h5>
<p><strong>coimhead an dèidh</strong> ‘look after’, ‘take care of’ (cf. ??):</p>
<ul>
  <li><strong>Chuir an t-ospadal Sandaidh còmhla ris airson <mark>coimhead às a dhèidh</mark>.</strong><br/>
    <small class="text-muted">‘The hospital sent Sandy with him to look after him.’ [#2 58]</small></li>
  <li><strong>Bha bràithrean Ruairidh a’ dol a <mark>choimhead às dèidh an taighe agus an sprèidh</mark>.</strong><br/>
    <small class="text-muted">‘Roddy’s brothers were going to look after the house and the animals.’ [#2 90]</small></li>
  <li><strong>Dh’iarr iad ormsa a dhol a Lunnainn a <mark>choimhead às dèidh ghnothaichean</mark>.</strong><br/>
    <small class="text-muted">‘They asked me to go to London to look after things.’ [#2 8]</small></li>
</ul>
<p><strong>coimhead airson</strong> ‘look for’ (cf. <strong>sir</strong>, <strong>lorg</strong>):</p>
<ul>
  <li><strong>Bha am poileas a’ <mark>coimhead airson Fiat bheag dhearg</mark>.</strong><br/>
    <small class="text-muted">‘The police were looking for a little red Fiat.’ [#2 59]</small></li>
</ul>
<p>Note that these two idioms are very close to being Beurlachas,</p>
