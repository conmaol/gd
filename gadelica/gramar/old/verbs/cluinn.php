<h1>cluinn</h1>
<p><strong>cluinn</strong> is an <a href="../../xx/irregularVerbs.php">irregular verb</a> corresponding to the English ‘hear’, and related to the verbs <strong><a href="eeist.php">èist</a></strong> ‘listen’ and <strong><a href="faic.php">faic</a></strong> ‘see’.</p>
