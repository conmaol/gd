<h3>Transitive uses</h3>
<p><strong>cluinn fuaim/guth/...</strong> – ‘hear a noise/voice/...’</p>
<ul>
  <li><strong><mark>Chuala iad am fuaim</mark> a rinn na balaich a’ briseadh doras na bùtha.</strong><br/>
    <small class="text-muted">‘They heard the noise that the boys made breaking (into) the shop door.’ [#2 53]</small></li>
  <li><strong>Nuair a dh’fhosgail searbhant an doras, <mark>chuala Anna guth feargach a peathar</mark> shuas an staidhre.</strong><br/>
    <small class="text-muted">‘When a servant opened the door, Ann heard her sister’s angry voice (from) upstairs.’ [#2 81]</small></li>
  <li><strong><mark>Chuala iad gàgail nan gèadh</mark> os cionn an taighe.</strong><br/>
    <small class="text-muted">‘They heard the cackling of the geese above the house.’ [#2 81]</small></li>
  <li><strong>“Cha <mark>chuala tu srann peileir</mark> a-riamh.”</strong><br/>
    <small class="text-muted">“You never heard the hum of a bullet.” [#2 92]</small></li>
  <li><strong>Aig an aon àm <mark>chuala e clag na fòn</mark>.</strong><br/>
    <small class="text-muted">‘At the same time he heard the ringing of the phone.’ [#2 121]</small></li>
</ul>
<p><strong>cluinn naidheachd/sgeulachd/fathann ...</strong> – ‘hear a story/rumour/...’</p>
<ul>
  <li><strong>Nuair a <mark>chuala an Deucon an naidheachd</mark>, thuirt e ...</strong><br/>
    <small class="text-muted">‘When the Deacon heard the news, he said ...’ [#2 115]</small></li>
  <li><strong>Ged a bhiodh sinn air <mark>fathann a chluinntinn</mark> mu dheidhinn, cha robh sinn air a chreidsinn.</strong><br/>
    <small class="text-muted">‘Although we had heard a rumour about it, we hadn’t believed it.’ [#2 113]</small></li>
  <li><strong>Ach cha robh duine an sin mun <mark>cuala mi uiread de sgeulachdan, no uirsgeulan</mark>, ’s a chuala mi mu dheidhinn Rob an Deucoin.</strong><br/>
    <small class="text-muted">‘But there was no-one there about whom I heard as many stories, or legends, as I heard about the Deacon’s Rob.’ [#2 112]</small></li>
  <li><strong>“Dè <mark>an dearbhadh a chuala sinn</mark> gu bheil Harry Heyford marbh?”</strong><br/>
    <small class="text-muted">“What proof did we hear that Harry Heyford is dead?” [#2 62]</small></li>
</ul>
“Chuala mi na naidheachdan aig ochd.” [#2 122]
 Ach nuair a chuala iad mo sgeulachd, mhothaich mi gun tug iad sùil air a chèile. [#2 15]

<p>Examples with pronoun objects:</p> 
<ul>
  <li><strong>“Nach <mark>cuala sinn seo</mark> roimhe?”</strong><br/>
    <small class="text-muted">“Haven’t we heard this before?” [#2 96]</small></li>
  <li><strong>Mus robh am foghar seachad reic e an oighreachd ’s cha <mark>chuala sinn an còrr</mark> mu dheidhinn.</strong><br/>
    <small class="text-muted">‘Before the autumn was out he sold the estate and we didn’t hear any more about him.’ [#2 77]</small></li>
  <li><strong><mark>Chluinneadh sin càil sam bith</mark> a thachradh san taigh mhòr.</strong><br/>
    <small class="text-muted">‘We would hear everything that happened in the big house.’ [#2 79]</small></li>
  <li><strong>Nuair a chuala iad sin cha robh iad ag iarraidh a’ chòrr.</strong><br/>
    <small class="text-muted">‘When they heard that they didn’t want anything else.’ [#2 100]</small></li>
</ul>
<p><strong>cluinn duine</strong> – ‘hear someone’</p>
<ul>
  <li><strong>“Thèid mi a-mach nuair a bhios mi deiseil,” arsa Ruairidh ann an guth ìosal gus nach <mark>cluinneadh an nighean e</mark> a bha san oifis eile.</strong><br/>
    <small class="text-muted">‘“I’ll go out when I’m ready,” said Roddy in a low voice so that the girl in the next office wouldn’t hear him.’ [#2 91-2]</small></li>
</ul>
“Suidh sìos,” ars esan ri Frank, ach cha do leig am balach air gun cuala e e. [#2 21-2]
Chuala mi e ag ràdh uair is uair rud a thug gàire orm. [#2 17]
Chuala mi dotair ag ràdh nach robh e a’ toirt do charaid dhomh ach sia mìosan eile [#2 18-19]
 Chuala mi uair is uair daoine a’ faighneachd, nam faiceadh tu do dhà leanabh ann an cunnart a bhith air am bàthadh, agus gun comas agad ach aon a shàbhaladh, dè fear a shàbhaladh tu?  [#2 46]

<p><strong>cluinn gun/nach ...</strong> – ‘hear that ...’</p>
<ul>
  <li><strong>“Ach nuair a <mark>chluinneas Wallace gu bheil</mark> thu marbh, dubhaidh e mach an t-iasad ’s thèid sinne a Dhùn Eideann.”</strong><br/>
    <small class="text-muted">“But when Wallace hears that you are dead, he will forget about the debt and we’ll go to Edinburgh.” [#2 56]</small></li>
  <li><strong>Bha Ted air <mark>cluinntinn gu robh</mark> Eilidh a’ coinneachadh Uisdein air an oidhche ann an taigh a màthar.</strong><br/>
    <small class="text-muted">‘Ted had heard that Helen had been meeting Hugh in the evenings at her mother’s house.’ [#2 81]</small></li>
  <li><strong>“<mark>Chuala mi nach eil</mark> làithean-saora gu bhith ann ... ”</strong><br/>
    <small class="text-muted">“I heard that there aren’t to be any holidays ... ” [#2 91]</small></li>
</ul>
<p>Examples with a source:</p>
<ul>
  <li><strong><mark>Chuala iad bho aon duine gu robh</mark> fear aige sa ghige a bha a’ freagairt tuairisgeul Mhangain.</strong><br/>
    <small class="text-muted">‘They heard from one man that he had had someone in his carriage who matched Manga’s description.’ [#2 84]</small></li>
  <li><strong>“<mark>Chuala mi air an rèidio gun ghoid</mark> iad sùim mhòr airgid.”</strong><br/>
    <small class="text-muted">“I heard on the radio that they stole a large sum of money.” [#2 101]</small></li>
</ul>
