<h3>Intransitive uses</h3>
<p><strong>cluinn bho dhuine</strong> – ‘hear from somebody’</p>
<ul>
  <li><strong>Dh’innis e dhomh gur e dotair a bha na athair agus gun <mark>cluinninn uaithe</mark> a dh’aithghearr.</strong><br/>
    <small class="text-muted">‘He told me that his father was a doctor and that I would hear from him soon.’ [#2 49]</small></li>
  <li><strong>Bha dòchas agam gun <mark>cluinninn uaithe</mark>.</strong><br/>
    <small class="text-muted">‘I was hoping that I would hear from him.’ [#2 50]</small></li>
</ul>
<p><strong>cluinn mu rud</strong> – ‘hear about something’</p>
<ul>
  <li><strong>Thug an duine ’s a bhean rum is biadh dha na balaich, ged a <mark>chuala iad an ùine ghoirid mun eucoir</mark> a rinn iad.</strong><br/>
    <small class="text-muted">‘The man and his wife gave them room and board, even though they heard about the crime they had committed shortly after.’ [#2 53]</small></li>
</ul>
<p><strong>cluinn mu dheidhinn duine</strong> – ‘hear of/about someone’</p>
<ul>
  <li><strong><mark>Chuala a h-uile duine sa choimhearsnachd againn mu dheidhinn Sandaidh</mark> agus a charaid Uisdean.</strong><br/>
    <small class="text-muted">‘Everybody in our community had heard of Sandy and his friend Hugh.’ [#2 52]</small></li>
  <li><strong>Bha a pheathraichean airson <mark>cluinntinn mu dheidhinn na h-ìghne</mark> a bha a’ fuireach còmhla ris.</strong><br/>
    <small class="text-muted">‘His sisters wanted to hear about the girl who was living with him.’ [#2 7]</small></li>
  <li><strong>“<mark>Chuala e an Glaschu mu dheidhinn fear</mark> a chuidicheadh duine aig an robh trioblaid le bhean.</strong><br/>
    <small class="text-muted">“He heard in Glasgow about a man who would help a man who was having trouble with his wife.” [#2 106]</small></li>
</ul>
Ach chuala sinn mu dheidhinn Frank. [#2 23]
Chuala fear-naidheachd bho bhean - ’s i an tidsear - mu dheidhinn aiste Mhata [#2 37]
