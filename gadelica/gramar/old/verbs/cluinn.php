    <small class="text-muted">‘I heard my friend say “God’s will” very often.’ [#2 119]</small></li>
  <li><strong><mark>Chuala mi fealla-dhà aige</mark> uair is uair.</strong><br/>
    <small class="text-muted">‘I heard him joking time and again.’ [#2 73]</small></li>
</ul>

