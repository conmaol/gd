<h1>barrachd</h1>
<p>cf. <strong><a href="tuilleadh.php">tuilleadh</a></strong>, <strong><a href="mooran.php">mòran</a></strong></p>
<hr/>
<p>Basic use – ‘more (money/information/...)’</p>
<ul>
  <li><strong>“Mas e sin a tha sibh ag iarraidh, cosgaidh e <mark>barrachd</mark>.”</strong><br/>
    <small class="text-muted">“If that’s what you want, it’ll cost more.” [#2 100]</small></li>
  <li><strong>Bha bràthair Syd na oifigear ann am prìosan Ghlaschu agus lorg esan <mark>barrachd</mark> mu dheidhinn Heyford.</strong><br/>
    <small class="text-muted">‘Syd’s brother was an officer in Glasgow prison and he found out more about Heyford.’ [#2 60]</small></li>
</ul>
<p><strong>barrachd stuth na ...</strong> ‘more stuff than ...’</p>
<ul>
  <li><strong>Dh’fhàg mi i a’ smaoineachadh gur dòcha gu robh <mark>barrachd fìrinn anns a’ phàipear-naidheachd na bha</mark> an Caiptean deònach aideachadh.</strong><br/>
    <small class="text-muted">‘I left her thinking that there might be more truth in the newspaper than the Captain was ready to admit.’ [#2 39]</small></li>
</ul>
<p><strong>barrachd air ...</strong> ‘more than just ...’, ‘something other than ...’</p>
<ul>
  <li><strong>Tha mi cinnteach nach do ghabh mi ach dà ghlainne uisge-beatha, ach feumaidh gu robh <mark>barrachd air uisge-beatha</mark> annta.</strong><br/>
    <small class="text-muted">‘I’m sure that I only had two glasses of whisky, but there must have been more than just whisky in them.’ [#2 48]</small></li>
</ul>
<p><strong>mòran a bharrachd stuth air ...</strong> ‘much more stuff than ...’</p>
<ul>
  <li><strong>Ged a bha i a cheart cho leisg ris, bha <mark>mòran a bharrachd tuigse aice air</mark> an t-Seilcheig.</strong><br/>
    <small class="text-muted">‘Although she was just as lazy as him, she had much more intelligence than the Slug.’ [#2 26]</small></li>
</ul>
<p><strong>barrachd air na is fhiach rud</strong> – ‘more than something is worth’</p>
<ul>
  <li><strong>“Tha fios a’m a-nise gur tric a tha an dìoghaltas a’ cosg <mark>barrachd air na ’s fhiach e</mark>.”</strong><br/>
    <small class="text-muted">“I know now that vengeance often costs more than it is worth.” [#2 64]</small></li>
  <li><strong>... a’ gabhail an airgid, <mark>mòran a bharrachd air na b’ fhiach a’ bhòidse</mark>.</strong><br/>
    <small class="text-muted">‘... taking the money, much more than the trip was worth.’ [#2 98]</small></li>
</ul>

<hr/>
<p><strong>cha/nach ... a bharrachd</strong> – ‘not ... either’, ‘neither’</p>
<ul>
  <li><strong>Chuir mi fòn chun na h-àireimh a thug Brian dhomh, ach cha robh a leithid a dh’àireamh ann. Fhuair mi a-mach glè aithghearr <mark>nach robh</mark> an Dr. Simon Simpson ann <mark>a bharrachd</mark>.</strong><br/>
    <small class="text-muted">‘I phoned the number Brian had given me, but there was no such number. I found out soon that there was no Dr Simon Simpson either.’ [#2 50]</small></li>
  <li><strong>Nuair a dhùisg mi bha mi anns an leabaidh agus cha robh sgeul oirrese. <mark>Cha robh</mark> sgeul air mo sheacaid <mark>a bharrachd</mark>.</strong><br/>
    <small class="text-muted">‘When I woke up I was in bed and there was no sign of her. There was no sign of my jacket either.’ [#2 48]</small></li>
  <li><strong>Mhothaich Ruairidh gu robh fuaim a’ chlò-sgrìobhadair anns an oifis eile air stad. <mark>Cha robh</mark> fuaim bho òrd no bho shàbh <mark>a bharrachd</mark>.</strong><br/>
    <small class="text-muted">‘Roddy noticed that the sound of the typewriter in the next office had stopped. There was no sound of a hammer or a saw either.’ [#2 91]</small></li>
</ul>
