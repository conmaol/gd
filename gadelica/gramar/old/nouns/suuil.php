<h1>sùil</h1>
<p><strong>sùil</strong> is a feminine noun.</p>

<h3>Idiomatic uses</h3>
<h5 id="thoir_suuil_air">thoir sùil (air)</h5>
<p>This idiom is similar in meaning to the verb of visual attention <strong><a href="../verbs/seall.php#intransitive">seall (ri)</a></strong> ‘look (at)’, and is often used with an adjective describing the kind of visual attention:</p>
<ul>
  <li><strong><mark>Thug Sam sùil gheur</mark> o dhuine gu bean.</strong><br/>
    <small class="text-muted">‘Sam looked sharply (gave a sharp glance) from man to woman.’ [#2 67]</small></li>
  <li><strong>Cha tuirt am fear-aiseig càil, ach <mark>thug e sùil glè neònach orm</mark>.</strong><br/>
    <small class="text-muted">‘The ferryman said nothing, but he looked at me very strangely (gave me a very strange glance).’ [#2 47]</small></li>
  <li><strong>Ach nuair a chuala iad mo sgeulachd, mhothaich mi gun <mark>tug iad sùil air a chèile</mark>.</strong><br/>
    <small class="text-muted">‘But when they heard my story, I noticed that they looked/glanced at each other.’ [#2 15]</small></li>
</ul>

