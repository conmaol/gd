  <li><strong>’S e Dan Iain Dhòmhnaill a <mark>dh’innis</mark> dhomh an sgeulachd seo <mark>o chionn ghoirid</mark>.</strong><br/> 
    <small class="text-muted">‘It was Dan John of Donald who told me this story recently.’ [#2 123]</small></li>
