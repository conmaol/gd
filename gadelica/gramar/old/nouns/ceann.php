<p><strong>(bh)o chionn ...</strong> ‘for/throughout some period of time’</p>
<ul>
  <li><strong>Cheannaich e an taigh a bha aig an Dotair Rae, taigh a <mark>bha</mark> fàs <mark>o chionn iomadach bliadhna</mark>.</strong><br/> 
    <small class="text-muted">‘a house which had been vacant for many years.’ [#2 85]</small></li>
</ul>
<p><strong>(bh)o chionn greis</strong> ‘for a while’</p>
<ul>
  <li><strong>Chuimhnich mi an uair sin gu <mark>robh</mark> càr beag air a bhith gam leantainn <mark>o chionn greis</mark>.</strong><br/> 
    <small class="text-muted">‘I recalled then that a small car had been following me for a while.’ [#2 50]</small></li>
  <li><strong>“<mark>Tha</mark> sinn an tòir orra <mark>bho chionn greis</mark>.”</strong><br/>
    <small class="text-muted">“We have been after them for a while.” [#2 101]</small></li>
  <li><strong>Bha a mhàthair san leabaidh. <mark>Bho chionn greis</mark> <mark>bha</mark> at air a h-amhaich.</strong><br/> 
    <small class="text-muted">‘His mother was in bed. For a while she had had a tumour on her neck.’ [#2 125]</small></li>
</ul>
<p><strong>(bh)o chionn fhada</strong> ‘for a long time’</p>
<ul>
  <li><strong>Thug Syd pìob luaidhe a <mark>bha</mark> e air ullachadh <mark>o chionn fhada</mark> à broilleach a sheacaid.</strong><br/> 
    <small class="text-muted">‘Syd took from the breast of his jacket a lead pipe that he had had prepared for a while.’ [#2 61]</small></li>
  <li><strong>“Nach bu chòir dhomh a <mark>bhith</mark> air fios fhaighinn <mark>o chionn fhada</mark>.”</strong><br/> 
    <small class="text-muted">“Shouldn’t I have known about this for a long time?” [#2 90]</small></li>
  <li><strong>Bha i a’ còmhradh cho aoibhneach ris, mar gum b’ e leannan a bh’ ann <mark>nach fhaca</mark> i <mark>o chionn fhada</mark>.</strong><br/> 
    <small class="text-muted">‘She was talking to him so gladly, as if he were a lover she hadn’t seen for a long time.’ [#2 108]</small></li>
  <li><strong><mark>Bha</mark> muinntir Thòmais air an clachan fhàgail <mark>o chionn fhada</mark>.</strong><br/> 
    <small class="text-muted">‘Thomas’s people had been gone from the village for a long time.’ [#2 5]</small></li>
</ul>
<p>A non-stative example:</p>
<ul>
  <li><strong><mark>Bhàsaich</mark> peathraichean a’ Chòirneil <mark>o chionn fhada</mark>.</strong><br/> 
