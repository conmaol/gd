<p><strong>coimhead/seall an dèidh duine/rud</strong> ‘look after someone/thing’ ‘take care of someone/thing’</p>
<ul>
  <li>Chuir an t-ospadal Sandaidh còmhla ris airson <mark>coimhead às a dhèidh</mark>. ‘The hospital sent Sandy along to look after him.’ [#2 58]</li>
  <li>Bha bràithrean Ruairidh a’ dol a <mark>choimhead às dèidh an taighe agus an sprèidh</mark>. ‘Roddy’s brothers were going to look after the house and the cattle.’ [#2 90]</li>
  <li>Dh’iarr iad ormsa a dhol a Lunnainn a <mark>choimhead às dèidh ghnothaichean</mark>. ‘They asked me to go to London to take care of arrangements.’ [#2 8]</li>
  <li>Mar as àbhaist bha mi a’ <mark>coimhead às dèidh an àite</mark> a cheart cho cùramach ’s a <mark>sheallainn às dèidh mo dhachaigh fhìn</mark>.
    ‘As always I looked after the place just as carefully as I would look aftermy own home.’ [#2 13]</li>
</ul>

