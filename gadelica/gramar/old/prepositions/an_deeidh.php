<p>Spatial sense? – <strong>an dèidh duine</strong></p>
<ul>
  <li>Bha iad a’ tighinn dhachaigh às an sgoil nuair a <mark>thàinig Land Rover às an dèidh</mark>.
    ‘They were coming home from school when a Land Rover came up behind them.’ [#2 75]</li>
  <li>Bha Rob a’ gàireachdainn, agus <mark>dh’èigh e às a dhèidh</mark>: “Leugh na Britheamhan 1-19.” [#2 6]</li>
  <li>Chrom iad an staidhre, <mark>duine an dèidh duine</mark>. [#2 119]</li>
  <li><mark>Ghlas mi an doras às mo dhèidh</mark> agus cha tug mi smaoin tuilleadh air a’ ghnothaich. [#2]</li>
</ul>
