<h3>Adverbial uses</h3>
<p><strong>an dèidh</strong> + event noun:</p>
<ul>
  <li><strong>Sin far an deach sinn <mark>às dèidh ar dìnneir</mark>.</strong><br/>
    <small class="text-muted">‘That is where we went after our dinner.’ [#2 48]</small></li>
  <li><strong><mark>An dèidh na deuchainne</mark> fhuair sinn na pàipearan air ais.</strong><br/>
    <small class="text-muted">‘After the test we got our papers back.’ [#2 71]</small></li>
</ul>
<p><strong>an dèidh</strong> + time period noun:</p>
<ul>
  <li><strong>Bha fios agam o chionn fhada gu robh Iain air a bhith ainmeil mar phoileas-lorg nan robh e air an dreuchd sin a roghnachadh, ach <mark>an dèidh dà fhichead bliadhna</mark> ... ?</strong><br/>
    <small class="text-muted">‘I knew that John would have been a famous detective if he had chosen that professions, but after 20 years ...?’ [#2 85]</small></li>
  <li><strong>Chaidh mi seachad an-dè air lios an Tàilleir <mark>an dèidh iomadach bliadhna air falbh bhon Chlachan</mark>.</strong><br/>
    <small class="text-muted">‘Yesterday I went past the Taylor’s garden after many years away from the village.’ [#2 72]</small></li>
</ul>
<hr/>
<p><strong>an dèidh seo/sin</strong>:</p>
<ul>
  <li><strong>Bhàsaich e às a chadal, agus ’s ann <mark>an dèidh sin</mark> a chaidh a’ mhearachd a dhèanamh.</strong><br/>
    <small class="text-muted">‘He died in his sleep, and it was after that that the mistake was made.’ [#2 19]</small></li>
</ul>
<p>With an adjective modifier:</p>
<ul>
  <li><strong><mark>Goirid an dèidh sin</mark> fhuair Ailig am fios ris an robh dùil aige.</strong><br/>
    <small class="text-muted">‘Shortly afterwards Alex got the news he had been expecting.’ [#2 107]</small></li>
  <li><strong><mark>Glè ghoirid an dèidh seo</mark>, fhuair mi fòn bho Mhàiri.</strong><br/>
    <small class="text-muted">‘Very shortly afterwards, I got a phone call from Mary.’ [#2 41]</small></li>
  <li><strong>’S ann <mark>fada an dèidh seo</mark> a thuig mi gu robh Aonghas air iuchar na ciste a lorg.</strong><br/>
    <small class="text-muted">‘It was long afterwards that I understood that Angus had found the key to the chest.’ [#2 33]</small></li>
</ul>
<p>With a time period modifier:</p>
<ul>
  <li>Bha an duine beò <mark>deich bliadhna an dèidh sin</mark>. [#2 18-19]</li>
  <li>’S ann <mark>dà fhichead bliadhna an dèidh sin</mark> a thàinig mo bhràthair dhachaigh. [#2 85]</li>
  <li><mark>Mu sheachdain an dèidh seo</mark> thàinig litir dhan oifis againn. [#2 39]</li>
  <li><mark>Mu dhà uair a thìde an dèidh sin</mark> thàinig an gille air ais. [#2 14]</li>
  <li><mark>Greis an dèidh seo</mark> thàinig litir bho Anna le dealbhan. [#2 80]</li>
</ul>
<hr/>
<p><strong>an dèidh làimhe</strong></p>
<ul>
  <li><mark>An dèidh làimhe</mark> bhriseadh e am bogha na phìosan. ‘Afterwards he would break the bow into pieces.’ [#2 120]</li>
</ul>
<hr/>
<p><strong>an dèidh làimhe</strong> + free relative clause</p>
<ul>
  <li>Thuirt i rinn gu robh i a’ dol air ais gu a càirdean “<mark>an dèidh na dh’fhuiling i</mark>.” ‘after what she had suffered’[#2 31]</li>
</ul>
<hr/>
<p><strong>an dèidh do dhuine rud a dhèanamh</strong> ‘after someone does something’</p>
<ul>
  <li>Aon fheasgar, <mark>an dèidh dha a bhith</mark> a’ coimhead air a bhean san ospadal, chaidh e a lorg taigh caraid.
    ‘One evening, after he had been to see his wife in hospital, he went to find a friend’s house.’ [#2 117]</li>
  <li>Am feasgar <mark>an dèidh do Phòl comanachadh</mark>, chunnaic Eilidh luch a’ ruith gu cùl an dreasair. 
    ‘The evening after Paul had taken communion, Helen saw a mouse running from behind the dresser.’  [#2 11]</li>
  <li>Dè chanadh am baile nan cluinneadh iad gu robh d’ athair a’ ruagadh luch <mark>an dèidh dha comanachadh</mark>. 
    ‘What would people say if they heard that your father had been chasing a mouse after he has taken communion.’ [#2 11]</li>
  <li><mark>Seachdain an dèidh dhaibh falbh</mark> thàinig bhan mhòr chun an taighe againn. ‘A week after they had left, a large van came to our house.’ [#2 13]</li>
  <li>Theich e le bheatha dhan Eilean Fhada, <mark>an dèidh dha Blàr Chùil Lodair a chall</mark>. 
    ‘He escaped with his life to the Long Island, after he had lost the Battle of Culloden.’ [#2 36]</li>
  <li>“Bha e beò <mark>fada an dèidh dhaibh dealachadh</mark>.” “He was alive long after they had parted.” [#2 39]</li>
</ul>
<p><strong>an dèidh rud a dhèanamh</strong> ‘after having done something’</p>
<ul>
  <li>... aig a’ phàrtaidh a rinn e fhèin agus Nan <mark>an dèidh a bhith pòsta</mark> leth-cheud bliadhna.
    ‘... at the party that he and Nan threw after having been married for 50 years.’ [#2 123]</li>
  <li>Nuair a thill mi dhachaigh <mark>an dèidh a bhith fichead bliadhna san RAF</mark> ...
    ‘When I returned home after having been in the RAF for 20 years, ...’ [#2 16]</li>
</ul>
<p><strong>bi an dèidh rud a dhèanamh</strong> ‘have done something’</p>
<ul>
  <li>Ann an 1945 <mark>bha e</mark> òg, agus <mark>an dèidh an cogadh a chur seachad san Nèibhi</mark>. 
    ‘In 1945 he was young and had spent the war in the Navy.’ [#2 123]</li>
</ul>



