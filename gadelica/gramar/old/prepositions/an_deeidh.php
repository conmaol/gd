<h1>an dèidh</h1>
<p><strong>an dèigh</strong> is a compound preposition corresponding to the English ‘after’, ‘later’.</p>
<p>A common alternative form is <strong>às dèidh</strong>. Other dialectal alternatives are <strong>an deaghaidh</strong>, <strong>às deaghaidh</strong>.</p>
<p>The conjugated forms are:</p>
