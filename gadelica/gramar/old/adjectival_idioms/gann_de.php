<h1>gann de</h1>
<p><strong>gann de</strong> is a predicate adjective expression corresponding to the English ‘short of’, ‘strapped for’, ‘lacking in’.</p>
<ul>
  <li>Bha iad gann de dh’uisge. ‘They were short of water.’ [CM 323]</li>
  <li>A bheil sinn cho gann de mhisneachd ’s gu bheil sinn aindeonach rud a dhèanamh? ‘Are we so lacking in courage that we are unwilling to do anything?’ [CM 323]</li>
  <li>Bha iad uile gann de dh’airgead. ‘They were all strapped for cash.’ [CM 323]</li>
</ul>
<p>As an adverbial:</p>
<ul>
  <li>Shiubhail e dìreach seachdain gann de na ceithir fichead bliadhna. ‘He died just a week short of eighty (years old).’ [CM 323]</li>
</ul>


