<h1>The copula <em>is</em></h1>
    <h2>Forms of the copula</h2>
    <p>The form of the copula depends on three different factors:</p>
    <ul>
      <li>whether the clause has <strong>positive</strong> or <strong>negative</strong> polarity</li>
      <li>whether the clause is <strong>declarative</strong> or <strong>interrogative</strong></li>
      <li>whether the clause has <strong>present/future</strong> tense or <strong>past/conditional</strong> tense.</li>
    </ul>
    <p>The forms of the copula are presented in the following table:</p>
    <table border="1">
