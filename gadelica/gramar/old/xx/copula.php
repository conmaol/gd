        <td>nach bu</td>
      </tr>
    </table>
<p>gur?</p>
    <p>Notes:</p>
    <ul>
      <li><em>is</em> is often written as <em>’s</em>.</li>
      <li><em>an</em> becomes am before labial consonants, i.e. <em>b-</em>, <em>f-</em>, <em>m-</em>, <em>p-</em>.</li>
      <li><em>cha</em> becomes <em>chan</em> before a vowel.</li>
      <li><em>bu</em> is usually written as <em>b’</em> before a vowel.</li>
    </ul>

