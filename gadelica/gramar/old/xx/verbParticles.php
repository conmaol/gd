<h1>Verb particles</h1>
<p>Gaelic has four verb particles:</p>
<table class="table table-bordered text-center" style="max-width:800px;">
  <tr><th></th><th colspan="2">declarative</th><th rowspan="2" class="align-middle">interrogative</th></tr>
  <tr><th></th><th>ungoverned</th><th>governed</th></tr>
  <tr><th>positive</th><td></td><td><strong><a href="../gd/particles/gun.php">gun</a></strong></td><td><strong><a href="../gd/particles/an.php">an</a></strong></td></tr>
  <tr><th>negative</th><td><strong><a href="../gd/particles/cha.php">cha</a></strong></td><td colspan="2"><strong><a href="../gd/particles/nach.php">nach</a></strong></td></tr>
</table>
<p>Verb particles govern either a <a href="dependency.php">dependent</a> verb or the past dependent verb marker <strong><a href="../gd/particles/do.php">do</a></strong>.</p>
<p>However, since the present/future <a href="copula.php">copula</a> form <strong>is</strong> does not have a dependent form of its own:</p>
<ul>
  <li>the verb particles <strong>cha</strong>, <strong>an</strong>, and <strong>nach</strong> can also be used as present/future copula forms</li>
  <li>there is a present/future copula form <strong>gur</strong>, corresponding to the verb particle <strong>gun</strong></li>
</ul>

