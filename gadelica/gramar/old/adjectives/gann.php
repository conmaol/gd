<h1>gann</h1>
<p><strong>gann</strong> is a predicative adjective corresponding to the English words ‘scarce’, ‘meagre’, ‘sparse’, ‘scanty’, ‘niggardly’, ‘spare’, ‘thin’, ‘few’.</p>
<hr/>
<p>Here are some simple examples:</p>
<ul>
  <li><strong>Bha an t-airgead <mark>gann</mark></strong>.<br/>
  <small class="text-muted">‘Money was tight.’ [#2 9]</small></li>
</ul>
<p>As an adverb:</p>
<ul>
  <li><strong>Bha an t-aodach air a ghearradh <mark>cho gann</mark> ris na fuaigheil</strong>.<br/>
  <small class="text-muted">‘The cloth was cut so niggardly up to the seams.’ [CM 323]</small></li>
</ul>
<p>Here are some compound expressions involving <strong>gann</strong>:</p>
<ul>
  <li><strong><a href="../adjectival_idioms/gann_de.php">gann de ...</a></strong> ‘short of’, ‘strapped for’, ‘lacking in’</li>
  <li><strong><a href="../copular_idioms/is_gann_gun.php">is gann gun ...</a></strong> ‘barely’, ‘hardly’, ‘scarcely’</li>
  <li><strong><a href="../adjectival_idioms/cha_ach_gann.php">cha ... ach gann</a></strong> ‘barely ever’, ‘hardly ever’, ‘scarcely ever’, ‘seldom’</li>
</ul>
<p>The following examples are interesting and need investigating:</p>
<ul>
  <li><strong>Is <mark>gann</mark> duine a tha air fhàgail.</strong><br/>
    <small class="text-muted">‘There are few people left.’ [CM 324]</small></li>
  <li><strong><mark>gann</mark> mìos an dèidh seo</strong><br/>
    <small class="text-muted">‘scarcely a month after this’ [CM 324]</small></li>
  <li><strong>Bha samhradh a bheatha seachad mun <mark>gann</mark> a thòisich e.</strong><br/>
    <small class="text-muted">‘The summer of his life was past before it had scarcely begun.’ [CM 324]</small></li>
</ul>
<p>The opposite of <strong>gann</strong> is <strong><a href="pailt.php">pailt</a></strong> ‘plentiful’, ‘abundant’, ‘copious’, ‘profuse’, ‘rife’, ‘sufficient’, ‘numerous’, ‘flush’.</p>

