<h1>pailt</h1>
<p><strong>pailt</strong> is a predicative adjective corresponding to the English words ‘plentiful’, ‘abundant’, ‘copious’, ‘profuse’, ‘rife’, ‘sufficient’, ‘numerous’, ‘flush’.</p>
<hr/>
<p>Here is an interesting example with <strong><a href="../particles/cho.php">cho</a></strong>:</p>
<ul>
  <li><strong>Bha e <mark>pailt cho math</mark> air dìreadh ’s a bha mise.</strong><br/>
  <small class="text-muted">‘He was every bit as good at climbing as I was.’ ‘He was fully as good at climbing as I was’ [CM 464]</small></li>
</ul>
<p>The opposite of <strong>pailt</strong> is <strong><a href="gann.php">gann</a></strong> ‘scarce’, ‘meagre’, ‘sparse’, ‘scanty’, ‘niggardly’, ‘spare’, ‘thin’, ‘few’ .</p>

