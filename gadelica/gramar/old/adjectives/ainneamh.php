<h1>ainneamh</h1>
<p>ainneamh is a predicative adjective corresponding to the English ‘scarce’, ‘rare’, ‘few’, ‘unusual’, ‘fine’, ‘excellent’, ‘uncommon’.</p>
<hr/>
<p>Basic examples:</p>
<ul>
  <li><strong>Tha sin <mark>ainneamh</mark>.</strong><br/>
    <small>‘That’s fine/excellent.’ [#2 ]</small></li>
  <li><strong>Agus ’s ann <mark>glè ainneamh</mark> a bhiodh sin.</strong><br/>
    <small>‘And that would be very rare.’ [CM 20]</small></li>
</ul>
<p>Some idiomatic uses:</p>
<ul>
  <li><strong><a href="../copular_idioms/is_ainneamh.php">is ainneamh ...</a></strong></li>
  <li><strong><a href="../copular_idioms/is_ann_ainneamh_a.php">is (ann) ainneamh a/nach ...</a></strong></li>
</ul>
<p>CM lists the derived feminine noun <strong>ainneamhag</strong> as meaning ‘phoenix’</p>
<ul>
  <li><strong>mar <mark>ainneamhag</mark> ag èirigh bhon luaithre</strong><br/>
    <small>‘like a phoenix rising from the ashes’ [#2 ]</small></li>
</ul>

