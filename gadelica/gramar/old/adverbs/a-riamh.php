<h1>a-riamh</h1>
<hr/>


<p><strong>cha/nach do thachair rud <mark>a-riamh</mark></strong> ‘had never happened’, ‘hadn’t ever happened’</p>
<ul>
  <li><strong><mark>Chan fhaca</mark> mi gin <mark>a-riamh</mark> cho àlainn.</strong><br/>
    <small class="text-muted">‘I had never see someone so beautiful.’ [#2 48]</small></li>
  <li>Cha d’ fhuair e a-riamh thairis air. ‘He never recovered from it.’ [57] FUTURE?</li>
  <li>Cha do thachair càil cho oillteil san eilean a-riamh. ‘had ever happened’ [60]</li>
</ul>




“An aithne dhut gun dh’ith siorc duine a-riamh?” “Do you know that a shark has ever eaten a man?” [63]

<p>Superlative:</p>
<ul>
  <li><strong>Sin nuair a rinn iad an eucoir agus an aingidheachd a b’ uabhasaiche a thachair <mark>a-riamh</mark> sa Chlachan.</strong><br/>
    <small class="text-muted">‘That is when they carried out the most terrible crime that had ever happened in the village.’ [#2 52]</small></li>
</ul>

<hr/>
<p><strong>cha/nach do thachair rud <mark>a-riamh roimhe</mark></strong> ‘someone had not happened ever before’, ‘something had never happened before’</p>
<ul>
  <li><strong><mark>Cha do thachair</mark> a leithid <mark>a-riamh roimhe</mark> san sgìre.</strong><br/>
    <small class="text-muted">‘That kind of thing had never happened before in the area.’ [#2 79]</small></li>
  <li><strong><mark>Cha do thachair</mark> eucoir cho gràineil sna h-eileanan <mark>a-riamh roimhe</mark> ...</strong><br/>
    <small class="text-muted">‘Such a heinous crime had never happened in the islands before.’ [#2 100]</small></li>
  <li><strong><mark>Cha do rinn</mark> luchd-turais seo <mark>a-riamh roimhe</mark>.</strong><br/>
    <small class="text-muted">‘Tourists had never done this before.’ [#2 98]</small></li>
  <li><strong>Bhiodh iad a’ cur geall air na h-eich tron phost, rud <mark>nach do rinn</mark> duine <mark>a-riamh roimhe</mark> san sgìre againne.</strong><br/>
    <small class="text-muted">‘They would be betting on the horses by post, something that nobody in our area had ever done before.’ [#2 52]</small></li>
  <li><strong>Ach cha b’ e ainm Dhoilein no m’ ainm fhìn a thàinig airson na treas duais ach ainm na Daolaig <mark>nach do choisinn</mark> duais <mark>a-riamh roimhe</mark>.</strong><br/>
    <small class="text-muted">‘But it was not Dolan’s name or my own name that came out for the third prize but the Daolag’s, who had not won a prize ever before.’ [#2 71]</small></li>
  <li><strong><mark>Cha do dh’innis</mark> mi do dhuine <mark>a-riamh roimhe</mark> e.</strong><br/>
    <small class="text-muted">‘I had never told it to anyone before.’ [#2 44]</small></li>
    
</ul>
<p><strong>an/nach do thachair rud a-riamh roimhe?</strong> ‘had something (n)ever happened before?’</p>
<ul>
  <li><strong>“Na thuit seòladair a-mach à long a-riamh roimhe?”</strong><br/>
    <small class="text-muted">“Has a sailor ever fallen from a ship before?” [#2 62]</small></li>
</ul>  
<p><strong>cha/nach robh ... <mark>a-riamh roimhe</mark></strong> ‘someone had not been the case ever before’</p>
<ul>
  <li><strong><mark>Cha robh</mark> dalta anns an sgìre againn <mark>a-riamh roimhe</mark>.</strong><br/>
    <small class="text-muted">‘There had not been a fosterling in our area ever before.’ [#2 21]</small></li>
</ul>

<p>Pluperfect example, cf. ‘cha do chuala mi guth a-riamh roimhe’:</p>
<ul>
  <li><strong><mark>Cha robh</mark> mi air guth a chluinntinn <mark>a-riamh roimhe</mark>.</strong><br/>
    <small class="text-muted">‘I hadn’t heard it mentioned ever before.’ [#2 40]</small></li>
</ul>





