    <h1>Càite</h1>
    <pre>
Càite a bheil a’ bhùth?
'Where is the shop?'

Càite a bheil an taigh-beag?
'Where is the toilet?'

Càite an do dh’fhalbh iad?
'Where did they go?'

Càite an do cheannaich thu na suiteis?
'Where did you buy the sweets?'

Càite an do chuir sibh an iuchair?
'Where did you put the key?'

Càite am fàg sinn an t-airgead?
‘Where shall we leave the money?’

Càite am bi sinn a’ dol a-nochd?
'Where will we be going tonight?'

Càite an cuireadh iad na beathaichean?
'Where would they put the animals?'

Càite an suidhicheadh tu na cùrsaichean sin?
'Where would you situate those courses?'

Càite an do rugadh sibh?
'Where were you born?'

Càite an d’fhuair sibh ur foghlam?
'Where were you educated?'

Càite a bheil an leabhar nach do leugh thu?
‘Where is the book you didn’t read?

Càite an tèid mi airson nan tiocaidean?
‘Where can I go for the tickets?’

A bheil fios agad càite a bheil an stèisean?
'Do you know where the station is?'

A bheil fios aca càite an deach e?
‘Do they know where he went?’

Tha fios agam càite an do chuir thu e.
‘I know where you put it.’
